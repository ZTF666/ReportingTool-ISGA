    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        int row = jt1.getSelectedRow();
        String log=t1.getText();
        String pwd=t2.getText();
        String role=jc1.getSelectedItem().toString();
        if(log.equals("") || pwd.equals("")){
            JOptionPane.showMessageDialog(null,
            "One or multiple text field empty !",
            "Error",
            JOptionPane.ERROR_MESSAGE);
        }else{
             String id=jt1.getModel().getValueAt(row, 0).toString();
     String login = jt1.getModel().getValueAt(row, 1).toString();
            DbInteraction.Maj("update users set login='"+log+"',pwd='"+pwd+"',role='"+role+"' where login='"+login+"' and id='"+id+"' ");
            JOptionPane.showMessageDialog(null,
            "User information updated",
            "Update informations",
            JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_UpdateActionPerformed

