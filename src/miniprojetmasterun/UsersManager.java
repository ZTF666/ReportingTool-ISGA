    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        
        int row = jt1.getSelectedRow();
if(row==-1){
    JOptionPane.showMessageDialog(null,
            "select a row please !",
            "Error",
            JOptionPane.ERROR_MESSAGE);
}else{
     int dialogButton = JOptionPane.YES_NO_OPTION;
int dialogResult = JOptionPane.showConfirmDialog (null, "Would you like to delete this user ?","Confirmation",dialogButton);
if(dialogResult == JOptionPane.YES_OPTION){
  
     String id=jt1.getModel().getValueAt(row, 0).toString();
     String login = jt1.getModel().getValueAt(row, 1).toString();
     
     DbInteraction.Maj("delete from users where login='"+login+"' and id='"+id+"'");
        JOptionPane.showMessageDialog(null,
            "User successfully deleted",
            "Delete user",
            JOptionPane.INFORMATION_MESSAGE);
        model.removeRow(row);
     
     }   
    }                                        
        
        
        
    }//GEN-LAST:event_DeleteActionPerformed

